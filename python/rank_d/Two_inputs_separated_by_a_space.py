# 文字列 s が 1 行で与えられるので s をそのまま出力

s = input()
print(s)

# 文字列 s と t が 2 行で与えられるので、s と t の 2 行をそのまま出力

s = input()
t = input()
print(s)
print(t)

# 半角スペースを含まない文字列 s が 1 行で与えられるので、そのまま出力

s = input()
print(s)

#  文字列 s_1, s_2 が半角スペースで区切られて 1 行で与えられる
#  各文字列を出力するごとに改行し 2 行で出力

s = input().split()
print(s[0])
print(s[1])