// whileによるループ処理
public class Main {
    public static void main(String[] args) {
        int i = 0;    // カウンタ変数の初期化
        while (i <= 9) {    // 0　-> 1 -> 2 -> 3 ・・・ 5 -> 6
            System.out.println("ハローpaizaラーニング");    // 繰り返し処理
            i = i + 1;    // カウンタ変数の更新
        }
    }
}

